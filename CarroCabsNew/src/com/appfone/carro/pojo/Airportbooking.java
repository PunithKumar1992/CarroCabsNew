package com.appfone.carro.pojo;

public class Airportbooking {
	
	private int airb_id;
	private String vehicle;
	private String source;
	private String destination;
	private String date;
	private String basicamt;
	private String gst;
	private String total;
	public int getAirb_id() {
		return airb_id;
	}
	public void setAirb_id(int airb_id) {
		this.airb_id = airb_id;
	}
	public String getVehicle() {
		return vehicle;
	}
	public void setVehicle(String vehicle) {
		this.vehicle = vehicle;
	}
	public String getSource() {
		return source;
	}
	public void setSource(String source) {
		this.source = source;
	}
	public String getDestination() {
		return destination;
	}
	public void setDestination(String destination) {
		this.destination = destination;
	}
	public String getDate() {
		return date;
	}
	public void setDate(String date) {
		this.date = date;
	}
	public String getBasicamt() {
		return basicamt;
	}
	public void setBasicamt(String basicamt) {
		this.basicamt = basicamt;
	}
	public String getGst() {
		return gst;
	}
	public void setGst(String gst) {
		this.gst = gst;
	}
	public String getTotal() {
		return total;
	}
	public void setTotal(String total) {
		this.total = total;
	}
	
	
	

}
