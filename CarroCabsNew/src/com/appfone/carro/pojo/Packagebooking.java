package com.appfone.carro.pojo;

public class Packagebooking {
	
	private int packbook_id;
	private String vehicle;
	private String packageselected;
	private String price;
	private String gst;
	private String total;
	private String cust_name;
	private String cust_phone;
	private String cust_email;
	private String cust_address;
	private String date;
	private String pickup_point;
	
	public int getPackbook_id() {
		return packbook_id;
	}
	public void setPackbook_id(int packbook_id) {
		this.packbook_id = packbook_id;
	}
	public String getVehicle() {
		return vehicle;
	}
	public void setVehicle(String vehicle) {
		this.vehicle = vehicle;
	}
	public String getPackageselected() {
		return packageselected;
	}
	public void setPackageselected(String packageselected) {
		this.packageselected = packageselected;
	}
	public String getPrice() {
		return price;
	}
	public void setPrice(String price) {
		this.price = price;
	}
	public String getGst() {
		return gst;
	}
	public void setGst(String gst) {
		this.gst = gst;
	}
	public String getTotal() {
		return total;
	}
	public void setTotal(String total) {
		this.total = total;
	}
	public String getCust_name() {
		return cust_name;
	}
	public void setCust_name(String cust_name) {
		this.cust_name = cust_name;
	}
	public String getCust_phone() {
		return cust_phone;
	}
	public void setCust_phone(String cust_phone) {
		this.cust_phone = cust_phone;
	}
	public String getCust_email() {
		return cust_email;
	}
	public void setCust_email(String cust_email) {
		this.cust_email = cust_email;
	}
	public String getCust_address() {
		return cust_address;
	}
	public void setCust_address(String cust_address) {
		this.cust_address = cust_address;
	}
	public String getDate() {
		return date;
	}
	public void setDate(String date) {
		this.date = date;
	}
	public String getPickup_point() {
		return pickup_point;
	}
	public void setPickup_point(String pickup_point) {
		this.pickup_point = pickup_point;
	}
	
	
	

}
